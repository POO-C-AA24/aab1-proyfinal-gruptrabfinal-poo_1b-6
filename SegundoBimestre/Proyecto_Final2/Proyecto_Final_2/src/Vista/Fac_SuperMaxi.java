    private void btnDeducilesCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeducilesCActionPerformed
       Factura factura = new Factura();
    DefaultTableModel modeloTabla = (DefaultTableModel) tblClientes.getModel();

    // Añadir productos a la factura desde la tabla
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        String nombreProducto = (String) modeloTabla.getValueAt(i, 0);
        double precio = Double.parseDouble(modeloTabla.getValueAt(i, 7).toString());
        int cantidad = Integer.parseInt(modeloTabla.getValueAt(i, 5).toString());
        String categoria = (String) modeloTabla.getValueAt(i, 6);
        
        Producto_Servicio producto;
        switch (categoria) {
            case "Vivienda":
                producto = new Vivienda(nombreProducto, precio, cantidad);
                break;
            case "Salud":
                producto = new Salud(nombreProducto, precio, cantidad);
                break;
            case "Alimentacion":
                producto = new Alimentacion(nombreProducto, precio, cantidad);
                break;
            case "Vestimenta":
                producto = new Vestimenta(nombreProducto, precio, cantidad);
                break;
            case "Educacion":
                producto = new Educacion(nombreProducto, precio, cantidad);
                break;
            default:
                throw new IllegalArgumentException("Categoría desconocida: " + categoria);
        }
        
        factura.agregarProducto(producto);
    }

    // Calcular los deducibles
    ArrayList<Double> deducibles = factura.calcularDeducibles();

    // Mostrar los deducibles en el campo de texto
    double totalDeducibles = 0.0;
    for (double deducible : deducibles) {
        totalDeducibles += deducible;
    }
    txtMontosCalcular.setText(String.format("%.2f", totalDeducibles));
    }//GEN-LAST:event_btnDeducilesCActionPerformed

