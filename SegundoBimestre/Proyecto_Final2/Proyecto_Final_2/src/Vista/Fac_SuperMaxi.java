    private void txtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyTyped
        char validacioncantidad = evt.getKeyChar();
        if (Character.isLetter(validacioncantidad)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Ingresar solo numeros", "Error", JOptionPane.ERROR_MESSAGE);
        }
        if (Character.isDigit(validacioncantidad)) {
            // Obtener el texto actual del campo de texto
            String input = txtCantidad.getText().trim();

            // Concatenar el nuevo carácter al texto actual
            String nuevoInput = input + validacioncantidad;

            // Verificar si el número está en el rango de 1 a 100
            int numero;
            try {
                numero = Integer.parseInt(nuevoInput);
            } catch (NumberFormatException e) {
                // Si no se puede convertir a número, entonces el input no es válido
                getToolkit().beep();
                evt.consume();
                JOptionPane.showMessageDialog(this, "Ingresar solo números entre 1 y 100", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (numero < 1 || numero > 100) {
                getToolkit().beep();
                evt.consume();
                JOptionPane.showMessageDialog(this, "Ingresar solo números entre 1 y 100", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Si no es un dígito, consumir el evento
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Ingresar solo números entre 1 y 100", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtCantidadKeyTyped

