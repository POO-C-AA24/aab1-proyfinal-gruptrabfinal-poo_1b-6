    public static boolean validarcedula(String input) {

        if (input.matches("\\d{10}")) {
            int[] A = new int[10];
            int sumpares = 0, sumimpar = 0, s, mul, sumat;

            for (int i = 0; i < 10; i++) {
                A[i] = Character.getNumericValue(input.charAt(i));
            }

            for (int i = 0; i < 9; i += 2) {
                mul = A[i] * 2;
                if (mul > 9) {
                    mul -= 9;
                }
                sumpares += mul;
            }

            for (int i = 1; i < 9; i += 2) {
                sumimpar += A[i];
            }

            sumat = sumpares + sumimpar;
            s = 10 - (sumat % 10);

            if (s == 10) {
                s = 0;
            }

            return s == A[9];
        } else {
            return false;
        }
    }

