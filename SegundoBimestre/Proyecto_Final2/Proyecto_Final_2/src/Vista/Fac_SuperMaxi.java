    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        DefaultTableModel modeloTabla = (DefaultTableModel) tblClientes.getModel();

        // Variable para el subtotal
        double subtotal = 0.0;

        // Sumar los valores de la columna 7
        for (int i = 0; i < modeloTabla.getRowCount(); i++) {
            // Obtener el valor de la columna 7 (precio)
            double precio = (double) modeloTabla.getValueAt(i, 7);

            // Sumar al subtotal
            subtotal += precio;
        }

        double porcentajeIva = 0.15;
        double iva = subtotal * porcentajeIva;
        double total = subtotal + iva;

        // Mostrar los resultados en los campos de texto
        txtSubTotal.setText(String.format("%.2f", subtotal));
        txtIva.setText(String.format("%.2f", iva));
        txtTotal.setText(String.format("%.2f", total));
        btnDeducilesC.setEnabled(true);
    }//GEN-LAST:event_btnCalcularActionPerformed

